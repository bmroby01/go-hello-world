package main

import "fmt"

func main() {
  fmt.Println("Hello, World!")

  fmt.Println("Bri" + "anna")

  fmt.Println("1+1=", 1+1)

  fmt.Println(true && false)
  fmt.Println(true || false)

  var a = "initial"
  fmt.Println(a)

  var b, c int = 1, 2
  fmt.Println(b, c)

  var d = true
  fmt.Println(d)

  var e int
  fmt.Println(e)

  f := "short"
  fmt.Println(f)
}
